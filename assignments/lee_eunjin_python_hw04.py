# 2. 시작단을 입력하고, 종료단을 입력하면 구구단을 출력하는 코드 작성

#     시작단 : 2
#     종료단 : 9

# *** 2단 ***
# 2 * 1 = 2
# ...
# 2 * 9 = 18
# *** 3단 ***
# ...
# 9 * 9 = 81

start = int(input("시작단 : "))
end = int(input("종료단 : "))

for dan in range(start, end + 1):
    print("*** " + str(dan) + "단 ***")
    
    for num in range(1, 10):
        result = dan * num
        print(str(dan) + " * " + str(num) + " = " + str(result))

