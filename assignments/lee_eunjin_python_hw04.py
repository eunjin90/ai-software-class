# 1. 2 ~ 100 사이의 소수를 출력하는 코드를 작성 (단, 한라인에 5개씩 출력)

#     2   3   5   7   11
#     13  17  19  23
#     ...
#     97

count = 0

for num in range(2, 101):

    i = 2
    while i < num and num % i != 0:
        i += 1

    if i == num:
        print("%4d" % num, end=" ")
        count += 1

        if count % 5 == 0:
            print()

