# 3. 구구단을 출력하는데 한 라인에 3단씩 출력
#     *** 2단 ***          *** 3단 ***        *** 4단 ***
#     2 * 1 = 2           3 * 1 = 3           4 * 1 = 4
#     ...
#     *** 5단 ***          *** 6단 ***        *** 7단 ***
#     *** 8단 ***          *** 9단 ***

#     rot dan in range(2, 10):

dans = [2,3,4,5,6,7,8,9]

for i in range(0, len(dans), 3):

    # 단 제목
    for j in range(i, i+3):
        if j < len(dans):
            print("%17s" % ("*** " + str(dans[j]) + "단 ***"), end="")
    print()

    # 곱하기 부분
    for num in range(1,10):
        for j in range(i, i+3):
            if j < len(dans):
                text = str(dans[j]) + " * " + str(num) + " = " + str(dans[j]*num)
                print("%17s" % text, end="")
        print()

    print()


